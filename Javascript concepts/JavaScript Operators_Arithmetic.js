// JavaScript Arithmetic Operators

let a = 10;
let b = 3;

console.log("a =", a, "b =", b);

console.log("a + b =", a + b);   // 13
console.log("a - b =", a - b);   // 7
console.log("a * b =", a * b);   // 30
console.log("a / b =", a / b);   // 3.333...
console.log("a % b =", a % b);   // 1
console.log("a ** b =", a ** b); // 1000

let x = 5;
console.log("x =", x);
console.log("++x =", ++x);       // 6
console.log("x++ =", x++);       // 6 (then becomes 7)
console.log("x =", x);           // 7

let y = 5;
console.log("y =", y);
console.log("--y =", --y);       // 4
console.log("y-- =", y--);       // 4 (then becomes 3)
console.log("y =", y);           // 3

// Using var
var varX = 5;
var varY = 15;
var varZ = 25;
console.log(varX, varY, varZ);

// Using let
let letX = 5;
let letY = 15;
let letZ = 25;
console.log(letX, letY, letZ);

// Using const
const constX = 5;
const constY = 15;
const constZ = 25;
console.log(constX, constY, constZ);

// Undefined Variables
let letUndefinex;
var varUndefinex;
const constUndefinex = 10; // must be initlise while declaring const
console.log(letUndefinex, varUndefinex, constUndefinex);